#Escreva um algoritmo que recebe um número x e y e imprime x elevado y
#Lembre-se que ∗∗ representa a potência entre dois números em Python.

valor = input("Informe o número de X: ")
numX = int(valor)

valor = input("Informe o número de Y: ")
numY = int(valor)

elevado = numX ** numY

print("Resultado",elevado)
